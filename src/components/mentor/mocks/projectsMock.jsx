const generateTPVEvolution = (base, growth = 0.02) => {
  const arr = [];
  let val = base * 0.6;
  for (let i = 0; i < 24; i++) {
    val = val * (1 + growth + (Math.random() - 0.5) * 0.05);
    arr.push({ month: `M${i + 1}`, tpv: Math.round(val) });
  }
  return arr;
};

